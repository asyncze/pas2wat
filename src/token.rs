#[derive(Clone, Debug, PartialEq)]
pub enum Type {
  INTEGER,
  REAL,
  BOOLEAN,
}

#[derive(Clone, Debug, PartialEq)]
pub enum Token {
  // BOF,
  TYPE_SPEC(Type),
  INTEGER(i32),
  REAL(f64),
  RANGE(i32, i32),
  TRUE,
  FALSE,
  PLUS,
  MINUS,
  MULTIPLY,
  DIVIDE,
  EQUAL,
  NOT_EQUAL,
  GREATER_THAN,
  GREATER_EQUAL,
  LESS_THAN,
  LESS_EQUAL,
  ODD,
  LPAR,
  RPAR,
  LBRA,
  RBRA,
  ID(String),
  ASSIGN,
  BLOCK,
  SEMICOLON,
  COLON,
  COMMA,
  DOT,
  PROGRAM,
  VAR,
  CONST,
  PACKED,
  ARRAY,
  OF,
  INTEGER_DIV,
  INTEGER_MOD,
  BEGIN,
  END,
  EMPTY,
  WHILE,
  DO,
  IF,
  THEN,
  ELSE,
  WRITELN,
  EOF
}